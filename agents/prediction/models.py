"""Models for the Oracle prediction agent."""

from dataclasses import dataclass, field
from datetime import UTC, datetime


@dataclass
class Prediction:
    """A deterministic strategic prediction."""

    title: str
    probability: float
    reasoning: str
    recommendation: str

    def __post_init__(self) -> None:
        """Validate prediction values."""
        if not 0.0 <= self.probability <= 1.0:
            msg = "probability must be between 0.0 and 1.0."
            raise ValueError(msg)


@dataclass
class PredictionReport:
    """Structured prediction report derived from learning reports."""

    predictions: list[Prediction]
    summary: str
    generated_by: str
    created_at: datetime = field(default_factory=lambda: datetime.now(UTC))
    strongest_prediction: Prediction = field(init=False)

    def __post_init__(self) -> None:
        """Validate prediction report values."""
        if not self.predictions:
            msg = "predictions must contain at least one Prediction."
            raise ValueError(msg)

        self.strongest_prediction = max(
            self.predictions,
            key=lambda prediction: prediction.probability,
        )

    def to_markdown(self) -> str:
        """Return the prediction report as Markdown."""
        predictions = "\n".join(
            (
                f"- {prediction.title}: {prediction.probability} "
                f"({prediction.reasoning})"
            )
            for prediction in self.predictions
        )
        return (
            "# Prediction Report\n\n"
            f"Generated by: {self.generated_by}\n\n"
            f"Strongest Prediction: {self.strongest_prediction.title}\n\n"
            f"## Predictions\n\n{predictions}\n\n"
            f"## Summary\n\n{self.summary}"
        )
